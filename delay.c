/*****************************************************************************
* 文件名：delay.c
* 说  明：延时函数
*****************************************************************************/
#include "main.h"

/******************************************************
* 函数：延时timer毫秒（单片机：AT89C51、晶振频率：12MHz）
* 参数：timer要延时的毫秒数值
*******************************************************/
void delay_ms(uint timer)
{
    uchar j = 0;
    while(timer--)
    {
        for(j = 124; j>0; j--)
        {
	    ;
	}
    }
}
